您可以喺右邊見到錄音介面 （如果您用手機，個錄音介面應該喺網頁嘅下面）。
介面上面嘅黑色長條係咪嘅音量計。
當您錄完音， 您嘅錄音會出現喺下面嘅黑色長條裏面。
您可以透過撳「撳呢度開始錄音」個掣嚟開始錄音，撳多一次個掣就會停止錄音。
您每次結束一段錄音，您嘅錄音就會出現喺下面，您可以撳播放掣嚟聽返您啱啱錄嘅音。
由於一啲即時通訊軟件裏面嘅瀏覽器並唔支援呢個錄音介面，如果網頁出現錯誤通知，請更換瀏覽器並重新試一次。
今次嘅研究係為咗研究您用平常、自然嘅方式嚟講嘢嘅語音，喺錄音過程如果您有任何藉口誤，您可以直接重複當時講緊嘅句子，唔使重新開始錄音。
今次嘅研究唔需要您事前準備或者喺心裏面講一次，請用您最自然嘅講嘢速度嚟講嘢就可以。
如果您唔小心終止咗錄音或者喺錄緊音嘅時候唔小心撳咗「上傳錄音檔」，請您由文章停咗嗰度重新開始錄音。
