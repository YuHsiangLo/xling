<h5>
    個人資料保密：
</h5>
<p>
    您嘅錄音檔將會儲存喺一個加密保護嘅電腦用嚟作後續嘅分析 
    我哋亦都會將您嘅語料分享俾其他註冊嘅研究人員，並且會確保呢啲研究人員已經喺專屬嘅網站註冊咗佢哋嘅資料同埋只能夠將分享嘅語料儲存喺加密咗嘅電腦裏面。
    我哋唔會公開發布您嘅語料，但係您可以選擇俾唔俾我哋喺學術場合裏面（例如學術會議或者論文發表嘅附件）播放或者使用您嘅錄音。
    您嘅姓名同埋聯絡方式唔會同語料有連繫，而係同其他研究人員分享語料嘅時候我哋都唔會分享您嘅個人資料。 
    語料庫裏面我哋會以匿名嘅方式嚟儲存您嘅資料（例如MN-TW-1）。
    如果錄音裏面有可以用嚟辨別您嘅資訊，我哋將會刪除相關嘅內容。
</p>
<p>
    我哋嘅研究包括測量您錄音裏面嘅聲學性質，例如音量同埋音高，或者計算特定詞彙喺您錄音嘅時候出現嘅次數。 
    呢啲資料將會記錄喺一個試算表裏面，而我哋會公開部分嘅資料嚟分享研究數據。 
    喺呢個過程裏面，我哋唔會洩露您任何嘅個人資料，而您嘅錄音亦都唔會對外公開。 
</p>
<p>
    喺研究過程裏面，我哋會透過呢個網站以問卷嘅方式嚟收集您嘅匿名個人基本資料。 
    呢啲匿名資料會儲存喺一個位於英屬哥倫比亞嘅加密伺服器裏面。
    基於研究分析嘅需要，我哋會以匿名嘅方式公開嚟公開部分嘅基本資料。呢啲資料只會包括廣泛嘅資訊（例如年齡同埋性別），並唔會有任何可以辨識個人嘅資訊。
</p>
