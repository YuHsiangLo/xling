<h5>
    受試者篩選條件：
</h5>
    您受邀參加今次嘅研究主要係因為您係廣東話嘅母語人士。只要您係介乎18至65歲而且並冇任何語言障礙，任何廣東話嘅母語人士都可以參加今次嘅研究。 
    基於本校嘅學術倫理規範，未經過研究助理邀請嘅受試者嘅語料將無法納入今次嘅研究。 
    如果您唔確定您嘅狀況，請聯絡研究助理王俊汶嚟釐清相關嘅細節。
