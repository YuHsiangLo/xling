<h3>受試同意書：以跨語言語料庫研究語音變遷。</h3>
<hr>
<p>
    我哋想邀請您參加我哋嘅研究計劃並且提供您嘅錄音檔俾我哋嘅跨語言語料庫。您嘅語料可以幫助我哋去理解語言變遷、個體之間嘅細微語音差異、同埋人腦係點樣呈現同處理語音。 
    如果您同意參加今次嘅研究，您會需要執行以下嘅事項：
</p>
<ul>
    <li>線上回答一啲有關您嘅個人背景問題，例如您嘅出生年份、性別、識得嘅語言同埋而家居住緊嘅城市。</li>
    <li>錄製您自己讀一篇簡短嘅文章（大約5-6分鐘）。</li>
    <li>錄製一段您自己嘅自發性談話（大約5-10分鐘）。我哋會提供幾個問題俾您參考同埋回應。</li>
</ul>
<p>
    以上所有嘅項目都會喺呢個網站進行，您唔需要準備其他特別嘅錄音設備，而且係每個部分我哋會提供詳細嘅錄音操作指示。
</p>
<hr>
<p>
