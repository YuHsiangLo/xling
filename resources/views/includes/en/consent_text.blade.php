<h3>Consent form: Studying sound change in small cross-linguistic corpora</h3>
<hr>
<p>
    You are invited to contribute to a collection of speech recordings from different languages.
    These recordings will help us understand how languages change over time, how languages differ from each other with respect to small details of pronunciation, and, more generally, how speech is represented and processed in the brain.
    If you decide to participate in this study, you will be asked to:
</p>
<ul>
    <li>Answer a few simple questions about yourself through an online form (what is your year of birth, gender? what languages are you fluent in? what city do you currently reside in?);</li>
    <li>Record yourself reading a short passage (around 5-6 minutes long);</li>
    <li>Record yourself speaking spontaneously and casually for 5-10 minutes. You will be given a few conversation prompts to help you with this.</li>
</ul>
<p>
    All of this will be administered through this website – you will not need to use any other recording devices. You will be given clear instructions on how to record yourself.
</p>
