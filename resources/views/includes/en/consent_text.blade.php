<h5>
    Confidentiality:
</h5>
<p>
    Your audio recordings will be stored securely on encrypted and password-protected devices for future analysis.
    The database that we are creating will be available to other researchers as well through a closed registration-only website, and therefore these researchers will also have access to your recordings.
    These researchers will only be able to access your data if they agree to store it securely on encrypted and password-protected devices.
    Your recordings will not be shared publicly, but you may opt to let us use short portions of your recordings in the dissemination of our results (e.g., in conference talks or as attachments to published papers).
    Your name or contact details will never be associated with the recordings and they will not be disclosed to researchers that we share the data with.
    You will only be referred to using a participant ID, e.g., "KR-01".
    If you accidentally share any personally identifying information as part of your recording, we will mute the relevant portion of the recording and will discard the original file.
</p>
<p>
    As part of our research, we will collect acoustic measurements based on your speech—for instance, how loud a given sound is, or how high or low the pitch is at a given point in the recording.
    We may also count specific words or sounds in your recordings.
    These numbers will be stored in spreadsheets, and some of these spreadsheets will be available to the public to facilitate the sharing of research data.
    You will not be identifiable from this data, and we will not share the actual recordings with the public.
</p>
<p>
    The survey will be conducted through this website.
    The anonymized results will be saved to a server on the UBC campus and will also be saved on encrypted and password-protected devices.
    We may publish your survey results in a completely anonymized form as part of our analysis.
    As the survey results only include broad and generic information, you will not be identifiable from these results.
</p>
<hr>
<h5>
    Your Participation is Voluntary:
</h5>
<p>
    You may choose to withdraw from the research at any point.
    You can do so by (i) closing this webpage at any point before submitting your speech recordings or (ii) contacting [name of RA] by email at [RA's email].
    Your decision to withdraw from the study will have no effect on your relationship with the Principal Investigator, the research assistants, the Department of Linguistics, or UBC.
</p>
<p>
    If you do not agree to participate in this study, we will not store any data about you.
    If you request to withdraw from the research after you have submitted your recording, we will delete all of your data from our database and server.
    However, we will not be able to remove your anonymized data from published research articles or spreadsheets that have been made available to the public.
</p>
