You can see the recording tool on the right-hand side (or below on mobile).
The top black bars show the level of the incoming sound.
The bottom black bar will show your recording when it is complete.
You can click "Start recording" to initiate a recording.
Press the same button will stop the recording.
You recording will appear below after you finish it; you can hit the play button to listen to it.
We try to be accessible through as many platforms and browsers as possible.
If your browser is not supported you may get an error message when opening the recorder page.
Please try with another browser/device if one is available to you.
<strong>This webpage will not function when accessed through the integrated browser of some instant messaging services, please try a different browser.</strong>

We are interested in your natural speech.
If you stumble over a word, you do not need to restart your recording.
Simply repeat the sentence like you would in normal conversation.
Please read at a pace you find comfortable.
There is no need to rehearse before the recording.
If you accidentally end or submit a recording before it is finished, please start a new one and keep going until the end.
