你可以在右方看到錄音介面（若您使用手機，則錄音介面應在網頁下方）。介面上方的黑色長條為麥克風音量計，而當您錄音完畢時，您的錄音會出現於下方的黑色長條中。您可以透過點擊「開始錄音」鍵開始錄音，再次點擊此鍵則會停止錄音。每當您結束一段錄音，您的錄音將會出現在下方，您可以透過點擊播放鍵來聆聽您剛錄的音檔。
由於一部分即時通訊軟體內建的瀏覽器並不支援本錄音介面，若是網頁出現錯誤通知，請更換瀏覽器並重新試一次。
本研究旨在研究您以平常、自然方式說話時的語音，在錄音過程中若有任何口誤，您可以直接重複目前的句子，不必從頭開始錄音。本研究不需要您事先準備或是在心裡默念，以您最自然的語速說話即可。若您不小心終止錄音或是誤上傳錄音檔，請由文章該處重新開始錄音。
