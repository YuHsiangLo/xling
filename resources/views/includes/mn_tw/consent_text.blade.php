<h3>受試同意書：以跨語言語料庫研究語音變遷。</h3>
<hr>
<p>
    我們想邀您參加我們的研究計畫並提供您的錄音檔至我們的跨語言語料庫。您的語料可以幫助我們理解語言變遷、跨個體間的細微語音差異、以及人腦如何呈現並處理語音。
    若您同意參加本研究，您會須要執行以下事項：
</p>
<ul>
    <li>線上回答一些有關您個人背景的問題，如您的出生年、性別、熟悉的語言、以及目前居住的城市。</li>
    <li>錄製您個人閱讀一則簡短的文章（約5-6分鐘）。</li>
    <li>錄製5-10分鐘您個人的自發性談話。我們會提供幾道問題供您參考及回應。</li>
</ul>
<p>
    以上所有項目皆在本站進行，您不須要準備其他特別的錄音設備，且在每個部分我們也會提供詳細的錄音操作指示。
</p>
<hr>
<p>
    本研究的主持人為任職於英屬哥倫比亞大學（University of British Columbia）語言學系的助理教授Márton Sóskuthy博士以及系上的博士生羅郁翔。
</p>

