<h5>
    受試者篩選條件：
</h5>
    您受邀參加本研究因為您為中文的母語人士。任何中文的母語人士其您年齡介於XX-YY11並沒有任何語言相關之障礙皆可參加本研究。基於本研究學術倫理規範，未經研究助理邀請的受試者，其語料無法納入本研究。若您不確定您的狀況，請聯絡研究助理羅郁翔以釐清相關研究細節。
<hr>
<h5>
    受試者助益：
</h5>
    本研究對研究受試者並無直接的助益，但受試者的語料將有助於我們了解其母語以及廣泛的語言變遷。
<hr>
<h5>
    研究危害：
</h5>
    本研究對受試者並無任何研究危害。
<hr>
<h5>
    個資保密：
</h5>
<p>
    您的錄音檔將存於一加密保護的電腦以供往後分析。我們也會將您的語料分享與其他註冊的研究人員，而我們會確保這些研究人員已於專屬的網站註冊其資料並只能將分享的語料存於加密後的電腦中。我們不會公開發表您的語料，但您可以選擇是否讓我們在學術場合中（如學術會議或是論文發表的附件）播放或使用您的錄音片段。您的姓名及聯絡方式不會與語料連結，而在與其他研究人員分享語料時，我們也不會分享您的個人資料。在語料庫中我們會以匿名的方式儲存您的資料（如TAMN-01），並確保錄音中所有有關個人的資料將以匿名的形式處理。
</p>
<p>
    我們的研究包含測量您錄音中的聲學性質，如音量及音高，或是計算特定詞彙在您錄音中出現的次數。這些資料將記錄於一試算表中，而我們會釋出其中部分的資料以利公開性研究。我們不會洩漏任何您的任何個資，而您的錄音檔也不會對外公開。
</p>
<p>
    在研究過程中，我們會透網本網站以問卷的方式收集您個人的匿名基本資料。這些匿名資料將存於一位於UBC的加密伺服器及部分電腦中。若基於研究分析需要，我們會以匿名的方式公開您的基本資料，但這些資料只包含廣泛的資訊（如年齡及性別），並不包含任何可以辨識個人的資訊。
</p>
