<h5>
    受試者篩選條件：
</h5>
    您受邀參加本研究主因為您是中文的母語人士，任何中文的母語人士其您年齡介於18-65，且並無任何語言相關之障礙者皆可參加本研究。
    基於本校學術倫理規範，未經研究助理邀請之受試者，其語料無法納入本研究。
    若您不確定您的狀況，請聯絡研究助理羅郁翔以釐清相關細節。
<hr>
<h5>
    受試者助益：
</h5>
    本研究對研究受試者並無直接助益，但受試者的語料將有助於我們了解其母語以及廣泛的語言變遷。
<hr>
<h5>
    研究危害：
</h5>
    本研究對受試者並無任何研究危害。
<hr>
<h5>
    個資保密：
</h5>
<p>
    您的錄音檔將存於一加密保護的電腦以供後續分析。
    我們也會將您的語料分享與其他註冊的研究人員，並會確保這些研究人員已於專屬的網站註冊其資料且只能將分享的語料存於加密後的電腦中。
    我們不會公開發表您的語料，但您可以選擇是否讓我們在學術場合中（如學術會議或論文發表的附件）播放或使用您的錄音片段。
    您的姓名及聯絡方式不會與語料相連結，而在與其他研究人員分享語料時，我們也不會分享您的個資。
    語料庫中我們會以匿名的方式儲存您的資料（如MN-TW-1），並確保錄音中所有有關個人的細節將以匿名的形式處理。
</p>
<p>
    我們的研究包含測量您錄音中的聲學性質，如音量及音高，或是計算特定詞彙在您錄音中出現的次數。
    這些資料將記錄於一試算表中，而我們會釋出其中部分的資料以利公開性研究。
    我們不會洩漏您的任何個資，而您的錄音檔也不會對外公開。
</p>
<p>
    在研究過程中，我們會透過本站以問卷的方式收集您的匿名個人基本資料。
    此匿名資料會存於一位於英屬哥倫比亞大學的加密伺服器中。
    若基於研究分析需要，我們會以匿名的方式公開您的部分基本資料，但這些資料只會包含廣泛的資訊（如年齡及性別），並不會有任何可以辨識個人的資訊。
</p>
<hr>
<h5>
    自主參與：
</h5>
<p>
    您可以在研究的任何階段透過（i）在上傳您的錄音檔前關閉本網頁或是（ii）聯絡研究助理羅郁翔（roger.lo[at]ubc.ca）來終止參與。
    終止參與本研究並不會影響您和英屬哥倫比亞大學、研究主持人、研究助理、或是語言學系的關係。
</p>
<p>
    若您決定不參加本研究，我們將不會儲存您的任何資料。
    若您在研究後想退出並追回您的錄音檔，我們會將您所有的個資以及錄音檔於伺服器中移除，但若我們已將您的匿名資料於學術論文或試算表中發表，我們恕無法將這些資料移除。
</p>
